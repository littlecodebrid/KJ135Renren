<template>
  <el-dialog
    :title="!dataForm.id ? $t('user.add') : $t('user.modify')"
    :close-on-click-modal="false"
    :visible.sync="visible">
    <el-form :model="dataForm" :rules="dataRule" ref="dataForm" @keyup.enter.native="dataFormSubmit()" label-width="180px">
      <el-form-item :label="$t('userAddOrUpdate.username')" prop="userName">
        <el-input v-model="dataForm.userName" :placeholder="$t('userAddOrUpdate.username')"></el-input>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.password')" prop="password" :class="{ 'is-required': !dataForm.id }">
        <el-input v-model="dataForm.password" type="password" :placeholder="$t('userAddOrUpdate.password')"></el-input>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.comfirmPassword')" prop="comfirmPassword" :class="{ 'is-required': !dataForm.id }">
        <el-input v-model="dataForm.comfirmPassword" type="password" :placeholder="$t('userAddOrUpdate.comfirmPassword')"></el-input>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.email')" prop="email">
        <el-input v-model="dataForm.email" :placeholder="$t('userAddOrUpdate.email')"></el-input>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.mobile')" prop="mobile">
        <el-input v-model="dataForm.mobile" :placeholder="$t('userAddOrUpdate.mobile')"></el-input>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.roleList')" size="mini" prop="roleList">
        <el-radio-group v-model="dataForm.roleId">
          <el-radio v-for=" role in userRoleList" :key="role.roleId" :label="role.roleId">{{role.roleName}}</el-radio>
        </el-radio-group>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.dataRoleList')" size="mini" prop="dataRoleList">
        <el-radio-group v-model="dataForm.dataRoleId">
          <el-radio v-for=" dataRole in dataRoleList" :key="dataRole.dataRoleId" :label="dataRole.dataRoleId">{{dataRole.dataRoleName}}</el-radio>
        </el-radio-group>
      </el-form-item>
      <el-form-item :label="$t('userAddOrUpdate.status')" size="mini" prop="status">
        <el-radio-group v-model="dataForm.status">
          <el-radio :label="0">{{$t('userAddOrUpdate.disable')}}</el-radio>
          <el-radio :label="1">{{$t('userAddOrUpdate.normal')}}</el-radio>
        </el-radio-group>
      </el-form-item>
    </el-form>
    <span slot="footer" class="dialog-footer">
      <el-button @click="visible = false">{{$t('userAddOrUpdate.cancel')}}</el-button>
      <el-button type="primary" @click="dataFormSubmit()">{{$t('userAddOrUpdate.determine')}}</el-button>
    </span>
  </el-dialog>
</template>

<script>
  import { isEmail, isMobile } from '@/utils/validate'
  export default {
    data () {
      var validatePassword = (rule, value, callback) => {
        if (!this.dataForm.id && !/\S/.test(value)) {
          callback(new Error('密码不能为空'))
        } else {
          callback()
        }
      }
      var validateComfirmPassword = (rule, value, callback) => {
        if (!this.dataForm.id && !/\S/.test(value)) {
          callback(new Error('确认密码不能为空'))
        } else if (this.dataForm.password !== value) {
          callback(new Error('确认密码与密码输入不一致'))
        } else {
          callback()
        }
      }
      var validateEmail = (rule, value, callback) => {
        if (!isEmail(value)) {
          callback(new Error('邮箱格式错误'))
        } else {
          callback()
        }
      }
      var validateMobile = (rule, value, callback) => {
        if (!isMobile(value)) {
          callback(new Error('手机号格式错误'))
        } else {
          callback()
        }
      }
      return {
        visible: false,
        dataRoleList:[],
        userRoleList: [],
        dataForm: {
          userName: '',
          password: '',
          comfirmPassword: '',
          salt: '',
          email: '',
          mobile: '',
          roleId: '',
          dataRoleId:'',
          status: 1
        },
        dataRule: {
          userName: [
            { required: true, message: '用户名不能为空', trigger: 'blur' }
          ],
          password: [
            { validator: validatePassword, trigger: 'blur' }
          ],
          comfirmPassword: [
            { validator: validateComfirmPassword, trigger: 'blur' }
          ],
          email: [
            { required: true, message: '邮箱不能为空', trigger: 'blur' },
            { validator: validateEmail, trigger: 'blur' }
          ],
          mobile: [
            { required: true, message: '手机号不能为空', trigger: 'blur' },
            { validator: validateMobile, trigger: 'blur' }
          ]
        }
      }
    },
    methods: {
      init (id) {
        this.dataForm.id = id || 0
        this.$http({
          url: this.$http.adornUrl('/sys/role/select'),
          method: 'get',
          params: this.$http.adornParams()
        }).then(({data}) => {
          this.userRoleList = data && data.code === 0 ? data.list : []
          this.dataRoleList = data && data.code === 0 ? data.dataRoleList : []
        }).then(() => {
          this.visible = true
          this.$nextTick(() => {
            this.$refs['dataForm'].resetFields()
          })
        }).then(() => {
          if (this.dataForm.id) {
            this.$http({
              url: this.$http.adornUrl(`/sys/user/info/${this.dataForm.id}`),
              method: 'get',
              params: this.$http.adornParams()
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.dataForm.userName = data.user.username
                this.dataForm.salt = data.user.salt
                this.dataForm.email = data.user.email
                this.dataForm.mobile = data.user.mobile
                this.dataForm.status = data.user.status
                this.dataForm.roleId = data.user.roleId
                this.dataForm.dataRoleId = data.user.dataRoleId
              }
            })
          }
        })
      },
      // 表单提交
      dataFormSubmit () {
        this.$refs['dataForm'].validate((valid) => {
          if (valid) {
            this.$http({
              url: this.$http.adornUrl(`/sys/user/${!this.dataForm.id ? 'save' : 'update'}`),
              method: 'post',
              data: this.$http.adornData({
                'userId': this.dataForm.id || undefined,
                'username': this.dataForm.userName,
                'password': this.dataForm.password,
                'salt': this.dataForm.salt,
                'email': this.dataForm.email,
                'mobile': this.dataForm.mobile,
                'status': this.dataForm.status,
                'roleId':this.dataForm.roleId,
                'dataRoleId': this.dataForm.dataRoleId
              })
            }).then(({data}) => {
              if (data && data.code === 0) {
                this.$message({
                  message: '操作成功',
                  type: 'success',
                  duration: 1500,
                  onClose: () => {
                    this.visible = false
                    this.$emit('refreshDataList')
                  }
                })
              } else {
                this.$message.error(data.msg)
              }
            })
          }
        })
      }
    }
  }
</script>
